SELECT 
H.NameHotel, SUM(P.Amount) AS TotalRevenue
 FROM 
 Reservation AS R
 JOIN Payments AS P ON R.Res_ID = P.Res_ID
 JOIN Room AS RM ON R.Room_ID = RM.Room_ID
 JOIN Hotels AS H ON RM.Hotel_ID = H.Hotel_ID
WHERE 
 P.Status = 'Completed'
GROUP BY 
 H.NameHotel;


